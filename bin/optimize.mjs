#!/usr/bin/env node
// Offline analysis over the smartloop trace corpus. Pure data work — no LLM calls.
// (The one sanctioned LLM step — DSPy-optimizing the contract-compilation prompt
// against the exported dataset — runs only as an explicit user command with the
// user's own key, outside this engine. This tool prepares and applies its data.)
//
//   node bin/optimize.mjs --export-dspy [--out <file>]   trace corpus as a DSPy-ready JSONL dataset
//   node bin/optimize.mjs --pacing                       write learned pacing stats to global/addenda/smartloop-pacing.md
//   node bin/optimize.mjs --regret                       list tier-up suggestions (done runs that had to be redone)
//   node bin/optimize.mjs --apply-tiers                  write regret-derived tier floors to global/addenda/smartloop-tiers.md,
//                                                        clamped to the bounds in global/smartloop-bounds.json (required)
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const argv = process.argv.slice(2);
const arg = (name) => { const i = argv.indexOf(name); return i === -1 ? null : argv[i + 1]; };

// ---- trace corpus -----------------------------------------------------------------------
const runs = [];
const telemetryRoot = join(ROOT, 'telemetry');
if (existsSync(telemetryRoot)) {
  for (const machine of readdirSync(telemetryRoot)) {
    const p = join(telemetryRoot, machine, 'smartloop-runs.jsonl');
    if (!existsSync(p)) continue;
    for (const line of readFileSync(p, 'utf8').split('\n').filter(Boolean)) {
      try { runs.push({ machine, ...JSON.parse(line) }); } catch { /* skip bad line */ }
    }
  }
}
runs.sort((a, b) => (a.ts ?? '').localeCompare(b.ts ?? ''));
const median = (xs) => {
  if (!xs.length) return null;
  const s = [...xs].sort((a, b) => a - b);
  return s.length % 2 ? s[(s.length - 1) / 2] : (s[s.length / 2 - 1] + s[s.length / 2]) / 2;
};

if (argv.includes('--export-dspy')) {
  // One JSONL example per run: the raw material for offline optimization of the
  // contract-compilation instructions (inputs) against outcomes (labels).
  const out = runs.map((r) => JSON.stringify({
    task: r.slug,
    outcome: r.outcome,
    iters: r.iters,
    wall_s: r.wall_s,
    verdicts: r.verdicts ?? [],
    machine: r.machine,
    ts: r.ts,
  })).join('\n') + (runs.length ? '\n' : '');
  const outFile = arg('--out');
  if (outFile) {
    writeFileSync(outFile, out);
    console.log(`optimize: wrote ${runs.length} example(s) to ${outFile}`);
  } else {
    process.stdout.write(out);
  }
} else if (argv.includes('--pacing')) {
  if (runs.length === 0) { console.log('optimize: no runs recorded yet — pacing hints need data'); process.exit(0); }
  const done = runs.filter((r) => r.outcome === 'done');
  const lines = [
    '<!-- generated by bin/optimize.mjs --pacing — do not edit; regenerate from the trace corpus -->',
    '## Learned pacing hints (smartloop)',
    '',
    `Derived from ${runs.length} recorded run(s)${done.length !== runs.length ? ` (${done.length} done)` : ''}:`,
    '',
    `- median wall time per completed run: ${Math.round((median(done.map((r) => r.wall_s)) ?? 0) / 60)} min`,
    `- median iterations per completed run: ${median(done.map((r) => r.iters)) ?? 'n/a'}`,
    `- longest completed run: ${Math.round(Math.max(0, ...done.map((r) => r.wall_s)) / 60)} min`,
    '',
    'Use these as priors at sleep decisions: waits expected to exceed the median',
    'wall time favor parking (tier 3) over scheduled wakes.',
  ];
  const target = join(ROOT, 'global', 'addenda', 'smartloop-pacing.md');
  writeFileSync(target, `${lines.join('\n')}\n`);
  console.log(`optimize: wrote pacing hints from ${runs.length} run(s) to global/addenda/smartloop-pacing.md`);
} else if (argv.includes('--regret') || argv.includes('--apply-tiers')) {
  // Regret signal: a slug recorded `done` and later recorded again means the
  // verification that passed it was too weak — suggest a tier-up.
  const seenDone = new Map();
  const regrets = [];
  for (const r of runs) {
    if (seenDone.has(r.slug) && !regrets.some((x) => x.slug === r.slug)) {
      regrets.push({ slug: r.slug, first_done: seenDone.get(r.slug), redone: r.ts });
    }
    if (r.outcome === 'done') seenDone.set(r.slug, r.ts);
  }
  if (argv.includes('--regret')) {
    if (regrets.length === 0) {
      console.log('optimize: no regret found — no completed run was later redone');
    } else {
      console.log('optimize: tier-up suggestions (done, then redone):');
      for (const x of regrets) console.log(`  ${x.slug}: done ${x.first_done}, redone ${x.redone} — verify one tier higher next time`);
    }
  } else {
    // Self-tuning is bounded or it doesn't happen: a human declares floor/ceiling once;
    // the engine may only move tiers inside that window. The output is a git-tracked
    // file — every applied change is logged in history and revertible.
    const boundsFile = join(ROOT, 'global', 'smartloop-bounds.json');
    if (!existsSync(boundsFile)) {
      console.error('optimize: --apply-tiers requires global/smartloop-bounds.json '
        + '(e.g. {"floor": 1, "ceiling": 2}) — bounds are the safety contract, no defaults');
      process.exit(1);
    }
    let bounds;
    try { bounds = JSON.parse(readFileSync(boundsFile, 'utf8')); } catch { bounds = null; }
    if (!bounds || ![0, 1, 2].includes(bounds.floor) || ![0, 1, 2].includes(bounds.ceiling) || bounds.floor > bounds.ceiling) {
      console.error('optimize: smartloop-bounds.json must declare integer floor <= ceiling, each 0-2');
      process.exit(1);
    }
    const tier = Math.min(bounds.ceiling, Math.max(bounds.floor, 2)); // regret always argues for the top
    const target = join(ROOT, 'global', 'addenda', 'smartloop-tiers.md');
    const lines = [
      '<!-- generated by bin/optimize.mjs --apply-tiers — do not edit; bounds in global/smartloop-bounds.json -->',
      '## Learned tier floors (smartloop)',
      '',
      ...(regrets.length === 0
        ? ['No regret in the corpus — no tier floors apply.']
        : regrets.map((x) => `- ${x.slug}: verify at tier >= ${tier} (done ${x.first_done}, redone ${x.redone})`)),
    ];
    writeFileSync(target, `${lines.join('\n')}\n`);
    console.log(`optimize: applied ${regrets.length} tier floor(s) at tier ${tier} `
      + `(bounds ${bounds.floor}-${bounds.ceiling}) to global/addenda/smartloop-tiers.md`);
  }
} else {
  console.error('optimize: pass one of --export-dspy [--out <file>] | --pacing | --regret | --apply-tiers');
  process.exit(1);
}
